//7. Desarrollo un programa en JavaScript que nos permita leer un número entero y determine si es negativo.

//Se crea una variable a la cual se le da un valor.
let n1 = -10;

//Si la condicion es mayor a 0 entonces el numero es positivo.
//De caso contrario el numero es negativo.
if(n1 > 0){
    console.log("El número es positivo");
}else{
    console.log("El numero", n1, "es negativo");
}